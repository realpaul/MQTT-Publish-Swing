private void jButtonConnectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonConnectMouseClicked
// TODO add your handling code here:
    if (this.jButtonConnect.getText().equals("Connect")) {
        try {
            mqtt = new MQTTConnection(this.jTextFieldServerHost.getText(), this.jTextFieldPort.getText(), "PushServer", "PushServer");
            this.jButtonConnect.setText("Disconnect");
            this.jLabelServerStatus.setText("connected");
        } catch (Exception ex) {
            System.err.append("Connection Failed");
        }
    } else {
        mqtt.disconnect();
        this.jButtonConnect.setText("Connect");
        this.jLabelServerStatus.setText("offline");
    }
}//GEN-LAST:event_jButtonConnectMouseClicked

