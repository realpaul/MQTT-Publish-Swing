private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
// TODO add your handling code here:
    try {
        if (mqtt.isConnected()) {
            mqtt.publishToTopic(this.jTextFieldDeviceID.getText(), this.jTextArea1.getText());
            jLabelSentStatus.setText("Sent...");
        }
    } catch (Exception ex) {
        System.err.println("Send message failed.");
    } finally {
        Runnable disappearLabel = new Runnable() {

            public void run() {
                try {
                    Thread.sleep(3000);
                } catch (Exception ex) {
                }
                jLabelSentStatus.setText("");
            }
        };
        Thread thread = new Thread(disappearLabel);
        thread.start();
    }
}//GEN-LAST:event_jButton1MouseClicked

